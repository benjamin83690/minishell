#include "libft.h"

void	ft_bzero(void *dest, size_t len)
{
	unsigned char	*a;
	size_t			v;

	a = (unsigned char *)dest;
	v = 0;
	while (len > 0 && len-- != 0)
		a[v++] = '\0';
}
